
public class COURSE {
	private String name, description, number;
		
	public COURSE (String pname, String pnumber, String pdescription){
		name = pname;
		number = pnumber;
		description = pdescription;
	}

	
	public String getName() {

		return name;
	}
	
	public String getDescription() {

		return description;
	}
	
	public String getNumber() {

		return number;
	}
	
	
}
